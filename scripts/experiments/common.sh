get_image_root() {
  local dataset="$1"
  if [[ "$dataset" == "flickr" ]]; then
    echo "$FLICKR_IMAGE_ROOT"
  elif [[ "$dataset" == "coco" ]]; then
    echo "$COCO_IMAGE_ROOT"
  else
    echo "Unknown dataset: $dataset" >&2
    return 1
  fi
}

selection_method_from_name() {
  local method="$1"
  if [[ "$method" == "ours_baseline" ]]; then
    echo "baseline"
  elif [[ "$method" == "ours_full" ]]; then
    echo "proxy_opt"
  else
    echo ""
  fi
}

sanitize_component() {
  local value="$1"
  value="${value//\\/-}"
  value="${value//\//-}"
  value="${value// /_}"
  echo "$value"
}

stage_log() {
  local message="$1"
  echo "[$(date '+%F %T')] ${message}"
}

format_budget_tag() {
  local budget="$1"
  printf "size_%04d" "$budget"
}
